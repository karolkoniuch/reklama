<a name="csposter">1. Jak zamowic reklamy?</a><br /><br />
<p>1. Zaloguj się na stronie.</p>
<p>2. Kliknij w zakładkę "Kup Reklamę" , wyślij smsa na podany numer i wpisz w pustą rubryczkę kod zwrotny.</p>
<p>3. Następnie kliknij &nbsp;w zakładkę "Zleć reklamę" i wypełnij puste rubryczki.</p>
<p>4. Reklamy w &nbsp;czasie 30 minut powinny się ukazać &nbsp;w zakładce "Linki do twoich Reklam"</p>
<a name="cs-reklama">2. Co to jest CS-Reklama?</a><br /><br />
CS-Reklama to strona służąca do promowania twoich serwerów Counter-Strike, bądź strony z twoimi serwerami CS na stronach o podobnej tematyce.<br /><br />
<a name="dzialanie">3. Jak działa CS-Reklama?</a><br /><br />
CS-Reklama jest zautomatyzowanym serwisem, który zatwierdza nowe tematy z twoją zawartością postu. Ty podajesz nam opis i tytul, a my w przeciągu 12 godzin sprawdzamy zawartość czy nie zawiera niedozwolonej treści i wykonujemy reklame. <br /><br />
<a name="licencja">4. Punkty na CS-Reklama</a><br /><br />
Punkty można zakupić poprzez płatności elektroniczne lub SMS. Punkty powinny doładować się do 15 minut w przypadku płatności, natomiast SMS natychmiastowo.<br />
W tabelce poniżej możesz zobaczyć cene punktów adekwatną do ilości for.<br /><br />
<a name="licencja">5. Reklama została odrzucona?</a><br /><br />
Jeśli twoja reklama została odrzucona, to tylko  z 3 możliwych powodów.
<p>1. Nie wkleiłes kodu w odpowiednim formacie</p>
<p>2. Nie wypełniłes poprawnie rubryczek.</p>
<p>3. Treśc reklamy nie promuje Sieci/Serwera CS&nbsp;</p>
W tym przypadku zleć ponownie reklamę według regulaminu.
